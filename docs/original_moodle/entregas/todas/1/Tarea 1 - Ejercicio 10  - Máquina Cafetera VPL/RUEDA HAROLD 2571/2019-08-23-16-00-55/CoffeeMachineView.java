    /* Se declara como una clase interna el Cuadro de Dialogo para recargar los insumos de la cefetera*/
    public class DialogoCafetera extends JDialog {

        private JLabel lblCantidad;
        private JLabel lblCosto;
        private JTextField txtCantidad;
        private JTextField txtCosto;
        private JButton cmdAceptar;
        private JButton cmdCancelar;
        private boolean ok;

        public DialogoCafetera(JFrame owner, String prefijoInsumo) {
            super(owner, true);
            lblCantidad = new JLabel();
            lblCosto = new JLabel();
            txtCantidad = new JTextField();
            txtCosto = new JTextField();
            cmdAceptar = new JButton();
            cmdCancelar = new JButton();

            setDefaultCloseOperation(WindowConstants.HIDE_ON_CLOSE);
            setLayout(new GridLayout(3, 2));

            lblCantidad.setFont(new Font("Time New Roman", 1, 20));
            lblCantidad.setText("Cantidad de " + prefijoInsumo);
            lblCantidad.setHorizontalTextPosition(SwingConstants.CENTER);
            add(lblCantidad);

            txtCantidad.setBackground(new Color(255, 255, 255));
            txtCantidad.setFont(new Font("Time New Roman", 0, 20));
            txtCantidad.setHorizontalAlignment(JTextField.CENTER);
            txtCantidad.setText("0");
            add(txtCantidad);

            lblCosto.setFont(new Font("Time New Roman", 1, 20));
            lblCosto.setText("Precio $$$ de " + prefijoInsumo);
            lblCosto.setHorizontalTextPosition(SwingConstants.CENTER);
            add(lblCosto);

            txtCosto.setBackground(new Color(255, 255, 255));
            txtCosto.setFont(new Font("Time New Roman", 0, 20));
            txtCosto.setHorizontalAlignment(JTextField.CENTER);
            txtCosto.setText("0");
            add(txtCosto);

            cmdAceptar.setFont(new Font("Time New Roman", 1, 20));
            cmdAceptar.setText("Aceptar");
            add(cmdAceptar);
            cmdCancelar.setFont(new Font("Time New Roman", 1, 20));
            cmdCancelar.setText("Cancelar");
            add(cmdCancelar);

            //clase anónima para eventos de boton
            cmdAceptar.addActionListener(new ActionListener() {
                public void actionPerformed(ActionEvent evt) {
                    cmdAceptarActionPerformed(evt);
                }
            });

            //clase anónima para eventos de boton
            cmdCancelar.addActionListener(new ActionListener() {
                public void actionPerformed(ActionEvent evt) {
                    cmdCancelarActionPerformed(evt);
                }
            });

            pack();
            setResizable(false);
            setLocation(owner.getX() + (owner.getWidth() / 2 - this.getWidth() / 2), owner.getY() + (owner.getHeight() / 2 - this.getHeight() / 2));
            setTitle("Ingrese la Cantidad y Precio de " + prefijoInsumo);
            setVisible(true);
        }//fin constructor clase interna GUIApuesta

        /**
         * Evento click cmdAceptar
         */
        private void cmdAceptarActionPerformed(ActionEvent evt) {
            this.ok = true;
            this.setVisible(false);
        }//fin cmdAceptarActionPerformed

        /**
         * Evento click cmdCancelar
         */
        private void cmdCancelarActionPerformed(ActionEvent evt) {
            this.ok = false;
            this.setVisible(false);
        }//fin cmdCancelarActionPerformed

        public int getCantidad() {
            return Integer.parseInt(txtCantidad.getText());
        }

        public int getCosto() {
            return Integer.parseInt(txtCosto.getText());
        }

        public boolean ok() {
            return this.ok;
        }
    }//fin class DialogoTarifa
