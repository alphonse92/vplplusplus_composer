    private void actualizarGUI() {
        txtCafe.setText(String.valueOf(maquina.getCafe()));
        txtAzucar.setText(String.valueOf(maquina.getAzucar()));
        txtVasos.setText(String.valueOf(maquina.getVasos()));
        txtPrecioBaseCafe.setText(String.valueOf(maquina.getPrecioBaseCafe()));
        txtIngresos.setText(String.valueOf(maquina.getIngresos()));
        txtEgresos.setText(String.valueOf(maquina.getEgresos()));
        txtGananciasBrutas.setText(String.valueOf(maquina.getGananciasBrutas()));
        txtImpuestos.setText(String.valueOf(maquina.getImpuestos()));
        txtGananciasNetas.setText(String.valueOf(maquina.getGananciasNetas()));
        if (maquina.getGananciasBrutas() < 0) {
            txtGananciasBrutas.setForeground(new Color(255, 0, 0));
        } else {
            txtGananciasBrutas.setForeground(new Color(0, 0, 0));
        }
        if (maquina.getGananciasNetas() < 0) {
            txtGananciasNetas.setDisabledTextColor(new Color(255, 0, 0));
        } else {
            txtGananciasNetas.setDisabledTextColor(new Color(0, 0, 0));
        }
    }

